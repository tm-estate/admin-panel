/**
 *  @swagger
 *  /api/deal_types:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Deal_types]
 *      summary: Get all deal_types
 *      description: Get all deal_types
 *      responses:
 *        200:
 *          description: Deal_types list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Deal_types"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;
    const payload = await Deal_typesDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = ['id', 'titleRu', 'titleEn', 'titleTm'];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

